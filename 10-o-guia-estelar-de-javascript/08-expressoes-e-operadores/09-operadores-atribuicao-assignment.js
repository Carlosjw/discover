// Operadores de atribuição (assignment)
let x;

console.log(x); // undefined

// assignment
x = 1;
console.log(x);

// addition assignment
x += 2;
console.log(x);

// subtraction assignment
x -= 1;
console.log(x);

// multiplication assignment
x *= 2;
console.log(x);

// division assignment
x /= 2;
console.log(x);

// remainder, exponetiation
x %= 2;
console.log(x);

x **= 2;
console.log(x);