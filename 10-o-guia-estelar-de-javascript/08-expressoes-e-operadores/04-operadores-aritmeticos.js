// Operadores aritméticos

// multiplicação *
console.log(9 * 8);

// divisão /
console.log(81 / 9)

// soma
console.log(8 + 7);

// subtração
console.log(8 - 6);

// resto da divisão
console.log(5 % 2);

// incremento ++
let number = 0;
number++;
console.log(number);

// incrementar no console
console.log(++number);

// decremento
let decrement = 0;
console.log(--decrement)

// exponencial **
console.log(3 ** 3);